    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        String sdtnd = txtsdt.getText();
        String ho = txtho.getText();
        String ten =txtten.getText();
        String ngay = txtngaysinh.getText();
        int ma = Integer.parseInt(txtma.getText());
        NguoiDungmodel nd = new NguoiDungmodel(ma, sdtnd, ho, ten, ngay);
        if(NguoiDung_dao.capNhatNguoiDung(nd)==true){
            JOptionPane.showMessageDialog(this, "Sửa thành công","Thông báo",JOptionPane.OK_OPTION);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(this, "Sửa không thành công","Thông báo",JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_btnluuActionPerformed

    private void txtsdtFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtsdtFocusLost
        String sdt = txtsdt.getText();
        String reg="^0\\d{9}$" ;
        if(sdt.length()>0){
            if(!sdt.matches(reg)){
                JOptionPane.showMessageDialog(rootPane, "Số điện thoại phải là 10 số và bắt đầu bằng số 0");
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Không được để trống SĐT");
        }
    }//GEN-LAST:event_txtsdtFocusLost

