    private void HienThi() {
        
        ArrayList<NguoiDungmodel> dsng = NguoiDung_dao.layInfoNguoiDung(a);
        for (NguoiDungmodel ng : dsng){
            txtho.setText(ng.getHo());
            txtten.setText(ng.getTen());
            txtngaysinh.setText(ng.getNgaySinh());
            txtma.setText(String.valueOf(ng.getMaND()));
        }
    }
